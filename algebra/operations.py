def sum(num1, num2):
	"""
	This function takes the sum of two numbers.
	
	Paramaters
	----------
	num1, float
		A number.	
        num2, float
                A number.
	"""
	return num1 + num2 


def product(num1, num2):
	"""
	This function takes the product of two numbers.
	"""
	return num1 * num2
